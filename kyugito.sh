do
  echo "$d - KYUGITO" >> $dummyfile
  git add $dummyfile
  GIT_AUTHOR_DATE="${d}T12:00:00" GIT_COMMITTER_DATE="${d}T12:00:00" git commit -m "feat: fake contribution for $d"
done

echo "Done! Now you just need to git push to your Github repo."
